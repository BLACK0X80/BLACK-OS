void black_fb_init(uint32_t black_addr, uint32_t black_w, uint32_t black_h, uint32_t black_pitch, uint8_t black_bpp)
{
    black_fb.black_width = black_w;
    black_fb.black_height = black_h;
    black_fb.black_pitch = black_pitch;
    black_fb.black_bpp = black_bpp;

    uint32_t black_fb_size = black_pitch * black_h;
    for (uint32_t black_off = 0; black_off < black_fb_size; black_off += PAGE_SIZE) {
        black_vmm_map_page(0xE0000000 + black_off, black_addr + black_off, PAGE_PRESENT | PAGE_WRITE | PAGE_NOCACHE);
    }
    black_fb.black_addr = (uint32_t *)0xE0000000;
    black_fb.black_backbuffer = (uint32_t *)kmalloc(black_fb_size);
}

void black_fb_putpixel(int black_x, int black_y, uint32_t black_c)
{
    if (black_x < 0 || black_y < 0 || (uint32_t)black_x >= black_fb.black_width || (uint32_t)black_y >= black_fb.black_height) return;
    if (black_fb.black_backbuffer)
        black_fb.black_backbuffer[black_y * (black_fb.black_pitch / 4) + black_x] = black_c;
}

void black_fb_fillrect(int black_x, int black_y, int black_w, int black_h, uint32_t black_c)
{
    for (int black_j = black_y; black_j < black_y + black_h; black_j++) {
        for (int black_i = black_x; black_i < black_x + black_w; black_i++) {
            black_fb_putpixel(black_i, black_j, black_c);
        }
    }
}

void black_fb_drawchar(int black_x, int black_y, char black_ch, uint32_t black_fg, uint32_t black_bg)
{
    int black_idx = (int)(unsigned char)black_ch;
    if (black_idx >= 128) black_idx = '?';
    const uint8_t *black_glyph = black_font_8x8[black_idx];
    for (int black_row = 0; black_row < 8; black_row++) {
        for (int black_col = 0; black_col < 8; black_col++) {
            uint32_t black_color = (black_glyph[black_row] & (0x80 >> black_col)) ? black_fg : black_bg;
            black_fb_putpixel(black_x + black_col, black_y + black_row, black_color);
        }
    }
}

void black_fb_drawstring(int black_x, int black_y, const char *black_s, uint32_t black_fg, uint32_t black_bg)
{
    while (*black_s) {
        black_fb_drawchar(black_x, black_y, *black_s++, black_fg, black_bg);
        black_x += 8;
    }
}

void black_fb_swap(void)
{
    if (black_fb.black_backbuffer && black_fb.black_addr) {
        uint32_t black_sz = black_fb.black_pitch * black_fb.black_height;
        memcpy(black_fb.black_addr, black_fb.black_backbuffer, black_sz);
    }
}

void black_fb_clear(uint32_t black_c)
{
    if (black_fb.black_backbuffer) {
        uint32_t black_total = (black_fb.black_pitch / 4) * black_fb.black_height;
        for (uint32_t black_i = 0; black_i < black_total; black_i++)
            black_fb.black_backbuffer[black_i] = black_c;
    }
}

black_framebuffer_t *black_fb_get(void) { return &black_fb; }
uint32_t black_fb_rgb(uint8_t black_r, uint8_t black_g, uint8_t black_b) { return (black_r << 16) | (black_g << 8) | black_b; }

void black_fb_blit(int black_dx, int black_dy, int black_w, int black_h, uint32_t *black_src)
{
    for (int black_j = 0; black_j < black_h; black_j++) {
        for (int black_i = 0; black_i < black_w; black_i++) {
            black_fb_putpixel(black_dx + black_i, black_dy + black_j, black_src[black_j * black_w + black_i]);
        }
    }
}

void black_fb_scroll_up(int black_lines, uint32_t black_bg)
{
    if (!black_fb.black_backbuffer) return;
    uint32_t black_stride = black_fb.black_pitch / 4;
    uint32_t black_pixels = black_lines * 8;
    memmove(black_fb.black_backbuffer, black_fb.black_backbuffer + black_pixels * black_stride,
            (black_fb.black_height - black_pixels) * black_stride * 4);
    for (uint32_t black_y = black_fb.black_height - black_pixels; black_y < black_fb.black_height; black_y++) {
        for (uint32_t black_x = 0; black_x < black_fb.black_width; black_x++) {
            black_fb.black_backbuffer[black_y * black_stride + black_x] = black_bg;
        }
    }
}
